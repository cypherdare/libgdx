/** {@link AssetLoader} to load {@link TextureArrayAtlas} instances. Passing a {@link TextureAtlasLoader.TextureAtlasParameter} to
 * {@link AssetManager#load(String, Class, AssetLoaderParameters)} allows to specify whether the atlas regions should be flipped
 * on the y-axis or not.
 * @author mzechner */
public class TextureArrayAtlasLoader extends TextureAtlasLoader<TextureArrayAtlas, TextureAtlasLoader.TextureAtlasParameter<TextureArrayAtlas>> {
	public TextureArrayAtlasLoader (FileHandleResolver resolver) {
		super(resolver);
	}

	@Override
	protected Class<? extends Texture> getTextureType (){
		return TextureArray.class;
	}

	@Override
	protected TextureAtlas generateAtlas (TextureAtlasData data){
		return new TextureArrayAtlas(data);
	}
	
	//TODO loadDependencies needs to sift out pages that are redundant
}
